package com.green.java.ch03;

public class OperatorEx8 {
    public static void main(String[] args) {
        int a = 1_000_000; //정수값 적을 때 _언더바 는 아무런 영향이 없다.
        //주로 숫자 파악하기 위해서 사용한다.
        int b = 1_0_0_0_0_0_0;
        int c = 1000000;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);

    }
}
