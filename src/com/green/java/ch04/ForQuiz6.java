package com.green.java.ch04;

public class ForQuiz6 {
    public static void main(String[] args) {

        for (int i = 1; i < 10; i++) {
            for (int a = 2; a < 10; a++) {
                System.out.printf("%d x %d = %d\t", a, i, i * a);
            }
            System.out.println();
        }
    }
}





