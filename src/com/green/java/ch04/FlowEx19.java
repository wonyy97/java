package com.green.java.ch04;

public class FlowEx19 {
    public static void main(String[] args) {
        for (int i = 1; i <= 3; i++) {
            for (int z = 1; z <= 3; z++) {
                for (int k = 1; k <= 3; k++) {
                    System.out.println("" + i + z + k);
                }
            }
        }
    }
}
























